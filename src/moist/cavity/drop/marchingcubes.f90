      if (max_extent <= 0.0_wp) then
         max_level = 1
      else
         max_level = max(1, &
                         ceiling(log(max_extent/min_spacing)/log(2.0_wp)))
      end if
      coarse_spacing = max(1.5_wp, spacing)
      nx = max(1, ceiling(base_len/coarse_spacing))
      ny = nx
      nz = nx
      coarse_spacing = base_len/real(nx, wp)

