      integer :: loc_top, local_stack_size
      integer :: max_level
      real(wp) :: min_spacing
      real(wp) :: max_extent
      real(wp) :: base_len
      real(wp) :: coarse_spacing
      real(wp), parameter :: change_tol = 1.0e-3_wp
      logical :: all_pos, all_neg
      real(wp) :: coarse_area, coarse_vol
      logical :: do_refine
      integer :: n_cubes_processed
      integer :: coarse_tri, sub_tri
      type(cube_task) :: cube
      real(wp) :: mid(3)
      real(wp) :: sub_vals(8, 8)
      real(wp) :: sub_min(3, 8), sub_max(3, 8)
      real(wp) :: sub_area, sub_vol
      integer :: child
