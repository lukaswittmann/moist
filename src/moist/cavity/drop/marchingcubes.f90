   pure function vertex_interp(p1, p2, val1, val2) result(p)
      real(wp), intent(in) :: p1(3), p2(3)
      real(wp), intent(in) :: val1, val2
      real(wp) :: p(3)
      real(wp) :: t

      if (abs(val2 - val1) < tiny(1.0_wp)) then
         p = 0.5_wp*(p1 + p2)
      else
         t = -val1/(val2 - val1)
         p = p1 + t*(p2 - p1)
      end if
   end function vertex_interp

   !> Compute grid bounds from the atomic-radii extent plus a padding margin
   subroutine compute_lsf_grid_bounds(lsf, xyz, grid_min, grid_max, margin, spacing)
      class(moist_cavity_drop_lsf_type), intent(in) :: lsf
      real(wp), intent(in) :: xyz(:, :)
      real(wp), intent(out) :: grid_min(3), grid_max(3)
      real(wp), intent(in) :: margin
      real(wp), intent(in) :: spacing

      real(wp) :: mol_min(3), mol_max(3)
      real(wp) :: pad

      ! Compute molecular center and extent
      mol_min = [minval(xyz(1, :) - lsf%radii(:)), &
                 minval(xyz(2, :) - lsf%radii(:)), &
                 minval(xyz(3, :) - lsf%radii(:))]
      mol_max = [maxval(xyz(1, :) + lsf%radii(:)), &
                 maxval(xyz(2, :) + lsf%radii(:)), &
                 maxval(xyz(3, :) + lsf%radii(:))]

      ! Keep at least one grid spacing plus user margin beyond atoms
      pad = max(margin, spacing)
      grid_min = mol_min - pad
      grid_max = mol_max + pad

   end subroutine compute_lsf_grid_bounds

   !> Compute LSF isosurface area and volume with marching cubes
   !> The cavity is the f0=0 isosurface (negative inside). Triangle winding follows the
   !> standard table convention; volume is summed via signed tetrahedra w.r.t. the origin.
   !>
   !> Uses screened LSF evaluation for O(N) per-point cost.
   !> Initial coarse grid vertices are cached to avoid redundant evaluations.
   !>
   !> @param[in]  lsf             LSF level-set function primitive
   !> @param[in]  xyz             Atomic coordinates (3, natom)
   !> @param[out] area            Isosurface area
   !> @param[out] volume          Enclosed volume
   !> @param[in]  target_spacing  Finest grid spacing (optional, default 0.2)
   !> @param[in]  debug           Enable debug output (optional)
   !> @param[in]  verbosity       Verbosity level; >=2 enables progress output (optional)
   !> @param[in]  obj_file        Wavefront OBJ mesh output path (optional)
   !> @param[in]  pqr_file        PQR file output path with triangle centroids (optional)
